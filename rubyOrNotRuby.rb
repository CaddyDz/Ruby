first_name = "Derek"
last_name = "Banas"

full_name = first_name + last_name

middle_name = "Justin"

full_name = "{first_name} #{middle_name} #{last_name}"

full_name.to_i # Convert to an integer
full_name.to_f # Convert to a float
full_name.to_sym # Convert to a sym

# Escape sequences
# \\ Backslash
# \' Single-quote
# \" Double-quote
# \a Bell
# \b Backspace
# \f Formfeed
# \n Newline
# \r Carriage
# \t Tab
# \v Vertical tab